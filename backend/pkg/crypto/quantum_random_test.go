func TestQuantumRandomGenerator_GenerateRandomBytes(t *testing.T) {
	generator := NewQuantumRandomGenerator()
	_ = generator.Initialize()

	testCases := []struct {
		name   string
		length int
	}{
		{"small length", 8},
		{"medium length", 64},
		{"large length", 256},
	}

	for _, tc := range testCases {
		t.Run(tc.name, func(t *testing.T) {
			bytes, err := generator.GenerateRandomBytes(tc.length)
			if err != nil {
				t.Fatalf("GenerateRandomBytes failed: %v", err)
			}

			if len(bytes) != tc.length {
				t.Errorf("Expected %d bytes, got %d", tc.length, len(bytes))
			}

			allZeros := true
			for _, b := range bytes {
				if b != 0 {
					allZeros = false
					break
				}
			}
			if allZeros {
				t.Error("Generated bytes should not all be zero")
			}
		})
	}
}

func TestQuantumRandomGenerator_GenerateQuantumRandomString(t *testing.T) {
	generator := NewQuantumRandomGenerator()
	_ = generator.Initialize()

	config := &QuantumCaptchaConfig{
		Length:           6,
		CharacterSet:   "ABCDEFGHJKLMNPQRSTUVWXYZ23456789",
		NoiseLevel:       0.3,
		AntiAI:           true,
		QuantumEnhanced: true,
		Unpredictability: 0.95,
	}

	challenge, err := generator.GenerateQuantumRandomString(config)
	if err != nil {
		t.Fatalf("GenerateQuantumRandomString failed: %v", err)
	}

	if len(challenge.Characters) != 6 {
		t.Errorf("Expected 6 characters, got %d", len(challenge.Characters))
	}

	if len(challenge.NoisePattern) != 6 {
		t.Errorf("Expected 6 noise patterns, got %d", len(challenge.NoisePattern))
	}

	if challenge.Entropy <= 0 {
		t.Error("Entropy should be positive")
	}

	if challenge.QuantumBits <= 0 {
		t.Error("QuantumBits should be positive")
	}

	if challenge.Signature == "" {
		t.Error("Signature should not be empty")
	}

	if !challenge.PredictionResistant {
		t.Error("Challenge should be prediction resistant")
	}
}

