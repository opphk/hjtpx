func TestQuantumRandomGenerator_SampleQuantumBit(t *testing.T) {
	generator := NewQuantumRandomGenerator()

	samples := make([]byte, 1000)
	for i := 0; i < 1000; i++ {
		samples[i] = generator.sampleQuantumBit()
	}

	ones := 0
	for _, s := range samples {
		if s == 0xFF {
			ones++
		}
	}

	onesRatio := float64(ones) / float64(len(samples))

	if onesRatio < 0.3 || onesRatio > 0.7 {
		t.Errorf("Quantum bit distribution should be roughly 50/50, got %.2f%% ones", onesRatio*100)
	}
}

